#ifndef XY_H
#define XY_H

/*  ---------------------------------------------------------------------[<]-
    Struct: xy
    Synopsis: Представляє координати клітинки (x — рядок, y — стовпець).
    ---------------------------------------------------------------------[>]-*/
struct xy {
    /*  ---------------------------------------------------------------------[<]-
        Variable: x
        Synopsis: Рядок клітинки.
        ---------------------------------------------------------------------[>]-*/
    int x;

    /*  ---------------------------------------------------------------------[<]-
        Variable: y
        Synopsis: Стовпець клітинки.
        ---------------------------------------------------------------------[>]-*/
    int y;

public:
    /*  ---------------------------------------------------------------------[<]-
        Constructor: xy
        Synopsis: Ініціалізує координати клітинки.
        ---------------------------------------------------------------------[>]-*/
    xy(int x, int y);
};

#endif