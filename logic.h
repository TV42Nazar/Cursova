#ifndef LOGIC_H
#define LOGIC_H

#include "field.h"
#include <vector>

/*  ---------------------------------------------------------------------[<]-
    Class: logic
    Synopsis: Містить алгоритми перевірки та розміщення наметів на полі.
    Використовує статичні методи, які працюють з об'єктами типу field.
    ---------------------------------------------------------------------[>]*/
class logic {
public:

    /*  ---------------------------------------------------------------------[<]-
        Function: is_valid_tent_placement
        Synopsis: Перевіряє, чи можна поставити намет на позицію (x, y)
        без порушення правил.
        ---------------------------------------------------------------------[>]-*/
    static bool is_valid_tent_placement(field field, int x, int y);


    /*  ---------------------------------------------------------------------[<]-
        Function: backtrack_tents
        Synopsis: Використовує рекурсивний бектрекінг для розміщення наметів
        біля дерев. Повертає true, якщо вдалося розмістити всі намети.
        ---------------------------------------------------------------------[>]-*/
    static bool backtrack_tents(field field, const std::vector<xy>& trees, int index);


    /*  ---------------------------------------------------------------------[<]-
        Function: place_tents
        Synopsis: Обгортка для автоматичного розміщення наметів на полі
        через бектрекінг.
        ---------------------------------------------------------------------[>]-*/
    static void place_tents(field field);
};

#endif